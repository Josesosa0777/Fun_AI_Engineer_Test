from itertools import cycle

def xor_decrypt(hex_cipher: str, key: str) -> str:
    """
    Decrypts a hex-encoded ciphertext using XOR with a repeating key.

    Args:
        hex_cipher: The ciphertext as a hexadecimal string.
        key: The XOR key as a string.

    Returns:
        The decrypted message as a UTF-8 string.
    """
    data = bytes.fromhex(hex_cipher)
    key_bytes = key.encode()
    decoded_bytes = bytes(b ^ k for b, k in zip(data, cycle(key_bytes)))
    return decoded_bytes.decode('utf-8', errors='ignore')


